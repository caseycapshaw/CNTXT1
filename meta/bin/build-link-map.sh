#!/usr/bin/env bash
# build-link-map.sh — regenerate meta/link-map.md, the wikilink → file-path index.
# Resolves every [[target]] the KB uses: concept slugs, connection slugs,
# People names + aliases, Job titles + aliases. Run after adding/renaming any
# concept, connection, person, or job. Idempotent: overwrites meta/link-map.md.
set -euo pipefail
cd "$(dirname "$0")/../.."   # vault root (meta/bin -> meta -> root)
out="meta/link-map.md"
tmp="$(mktemp)"

emit() { printf '| `[[%s]]` | `%s` |\n' "$1" "$2" >> "$tmp"; }

# Concepts + connections: slug = filename
collect_simple() {
  local dir="$1"
  for f in "$dir"/*.md; do
    [ -e "$f" ] || continue
    emit "$(basename "$f" .md)" "$f"
  done
}

# People + Jobs: canonical name = filename, plus any aliases: [a, b] in frontmatter
collect_with_aliases() {
  local dir="$1"
  for f in "$dir"/*.md; do
    [ -e "$f" ] || continue
    local name; name="$(basename "$f" .md)"
    case "$name" in *TEMPLATE*) continue ;; esac
    emit "$name" "$f"
    local aliases; aliases="$(grep -m1 '^aliases:' "$f" 2>/dev/null | sed -E 's/^aliases:[[:space:]]*\[//; s/\][[:space:]]*$//' || true)"
    [ -z "$aliases" ] && continue
    local IFS=','; for a in $aliases; do
      a="$(printf '%s' "$a" | sed -E 's/^[[:space:]]+//; s/[[:space:]]+$//')"
      [ -n "$a" ] && emit "$a" "$f"
    done
  done
}

{
  echo "# Link map — wikilink → file path"
  echo
  echo "_Generated by \`meta/bin/build-link-map.sh\`. Do not hand-edit — regenerate after adding/renaming a concept, connection, person, or job._"
  echo
  echo "Resolves every \`[[target]]\` the KB uses (canonical names + registered \`aliases:\`). Use it to jump straight to a file instead of grepping."
  echo
  echo "| Wikilink | Path |"
  echo "| :-- | :-- |"
} > "$tmp"

collect_simple concepts
collect_simple connections
collect_with_aliases People
collect_with_aliases Jobs

# sort the table rows (after the 8 header lines), keep header intact
head -8 "$tmp" > "$out"
tail -n +9 "$tmp" | sort -f >> "$out"
rm -f "$tmp"

echo "wrote $out ($(($(wc -l < "$out") - 6)) link targets)"
